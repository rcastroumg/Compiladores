    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String VarString=jTextArea1.getText();
        try {
            //Instance del Scanner
                AnalisisLexico scan = new AnalisisLexico(new BufferedReader( new StringReader(VarString)));
                scan.lista=lista; /* Se igualan la lista del scanner con nuestra instancea actual*/
                            ComplexSymbolFactory sf = new ComplexSymbolFactory();
                            scan.setSymbolFactory(sf);
                 AnalisisSintactico sintactico=new AnalisisSintactico(scan,sf);
                             
                 sintactico.lista=scan.lista; /* Se igualan la lista del scanner con la del parser*/
                sintactico.parse(); /*Aqui se efectuara el parser*/
                jTextArea2.setText(sintactico.resultado);
                jTextArea3.setText("");
                for(Iterator<ErrorCompilador> it = lista.iterator(); it.hasNext();) {
                    ErrorCompilador val=it.next();
                        jTextArea3.append(val.Linea+","+val.Columna+" "+val.Descripcion+"\n");
                }
                JOptionPane.showMessageDialog(this, "Generacion de codigo en tres direcciones finalizada.");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "No se ejecuto el parser");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

   
